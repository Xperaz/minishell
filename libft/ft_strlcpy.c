#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t dstsize)
{
	size_t	i;
	size_t	l_src;

	i = 0;
	l_src = ft_strlen(src);
	if (!dstsize)
		return (l_src);
	while (src[i] && i < dstsize - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (l_src);
}
